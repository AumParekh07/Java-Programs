// WAP to print all even numbers btw 1 to 100 using while loop.
public class Even {
    public static void main(String[] args) {
        int n, i;
        i = 0;
        n = 100;
        System.out.print("Enter Value of n:");
        while (i <= n - 1) {
            i = i + 2;
            System.out.println(i);

        }
    }
}
